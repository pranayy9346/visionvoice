import { callGeminiGenerate } from "./gemini.client.js";
import { clamp, parseJsonResponse } from "./gemini.normalizers.js";
import { buildDecisionPrompt } from "./gemini.prompts.js";

export async function decideInteraction({
  query,
  conversationHistory,
  lastScene,
  imageAge,
}) {
  const model = process.env.GEMINI_MODEL_FLASH || "gemini-2.5-flash";
  const prompt = buildDecisionPrompt({
    query,
    conversationHistory,
    lastScene,
    imageAge,
  });

  const raw = await callGeminiGenerate({ model, parts: [{ text: prompt }] });
  const parsed = parseJsonResponse(raw);

  return {
    useCache: parsed?.useCache === true,
    needNewImage: parsed?.needNewImage !== false,
    confidence: clamp(Number(parsed?.confidence) || 0.5, 0, 1),
    reason:
      parsed?.reason?.trim() || "Decision generated by adaptive controller.",
  };
}
